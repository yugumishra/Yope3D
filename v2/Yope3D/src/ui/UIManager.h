#pragma once
#include <vulkan/vulkan.h>
#include <vector>
#include <memory>
#include <string>
#include "Label.h"
#include "Background.h"
#include "TexturedBackground.h"
#include "AnimatedBackground.h"
#include "CurvedBackground.h"
#include "TextBox.h"
#include "TextAtlas.h"
